    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        this.setVisible(false);
        new SearchRecipe().setVisible(true);
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cinfo", "root", "");
            Statement st = con.createStatement();
            String sql = "Select * from recipe";
            ResultSet rs = st.executeQuery(sql);
            
            while(rs.next()){
                String id = String.valueOf(rs.getInt("sno"));
                String dname = rs.getString("dname");
                String bdesc = rs.getString("bdesc");
                String username = rs.getString("username");
                
                String tbData[] = {id, dname, bdesc, username};
                DefaultTableModel tbModel = (DefaultTableModel)SearchRecipe.jTable1.getModel();
                tbModel.addRow(tbData);
            }
            
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

