    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        this.setVisible(false);
        new DeleteRecipe().setVisible(true);
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/cinfo", "root", "");
            String sql = "SELECT * from recipe WHERE username = ?";
            PreparedStatement pst = null;
            pst = con.prepareStatement(sql);
            pst.setString(1, LoginFrame.username.getText());
            ResultSet rs = pst.executeQuery();
            
            while(rs.next()){
                String id = String.valueOf(rs.getInt("sno"));
                String dname = rs.getString("dname");
                String bdesc = rs.getString("bdesc");
                String username = rs.getString("username");
                
                String tbData[] = {id, dname, bdesc, username};
                DefaultTableModel tbModel = (DefaultTableModel)DeleteRecipe.jTable1.getModel();
                tbModel.addRow(tbData);
            }
            
            con.close();
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton3ActionPerformed

